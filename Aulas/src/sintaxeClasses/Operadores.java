package sintaxeClasses;

public class Operadores {

	public static void main(String[] args) {
		String  concatenaçao = "?";
		
		concatenaçao = 1+1+1+"1";
		
		System.out.println(concatenaçao);
		
		concatenaçao = 1+"1"+1+1;
		
		System.out.println(concatenaçao);
		
		concatenaçao = 1+"1"+1+"1";
		
		System.out.println(concatenaçao);
		
		concatenaçao = "1"+1+1+1;
		
		System.out.println(concatenaçao);
		
		concatenaçao = "1"+(1+1+1);
		
		System.out.println(concatenaçao);
	
	
	}
 
}
